package com.example.attendancetracker.ui.hoddetails;

public class HOD {
    private String Name_of_HOD,EmpCode_of_HOD,Phone_of_HOD,College;

    public HOD() {
    }

    public String getCollege() {
        return College;
    }

    public void setCollege(String college) {
        College = college;
    }

    public String getName_of_HOD() {
        return Name_of_HOD;
    }

    public void setName_of_HOD(String name_of_HOD) {
        Name_of_HOD = name_of_HOD;
    }

    public String getEmpCode_of_HOD() {
        return EmpCode_of_HOD;
    }

    public void setEmpCode_of_HOD(String empCode_of_HOD) {
        EmpCode_of_HOD = empCode_of_HOD;
    }

    public String getPhone_of_HOD() {
        return Phone_of_HOD;
    }

    public void setPhone_of_HOD(String phone_of_HOD) {
        Phone_of_HOD = phone_of_HOD;
    }
}

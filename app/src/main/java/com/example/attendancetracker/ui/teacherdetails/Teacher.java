package com.example.attendancetracker.ui.teacherdetails;

public class Teacher {

    private String Name_of_Teacher,EmpCode_of_Teacher,Phone_of_Teacher;
    String college,dept,year,semester;
    public Teacher() {
    }

    public String getCollege() {
        return college;
    }

    public void setCollege(String college) {
        this.college = college;
    }

    public String getDept() {
        return dept;
    }

    public void setDept(String dept) {
        this.dept = dept;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getSemester() {
        return semester;
    }

    public void setSemester(String semester) {
        this.semester = semester;
    }

    public String getName_of_Teacher() {
        return Name_of_Teacher;
    }

    public void setName_of_Teacher(String name_of_Teacher) {
        Name_of_Teacher = name_of_Teacher;
    }

    public String getEmpCode_of_Teacher() {
        return EmpCode_of_Teacher;
    }

    public void setEmpCode_of_Teacher(String empCode_of_Teacher) {
        EmpCode_of_Teacher = empCode_of_Teacher;
    }

    public String getPhone_of_Teacher() {
        return Phone_of_Teacher;
    }

    public void setPhone_of_Teacher(String phone_of_Teacher) {
        Phone_of_Teacher = phone_of_Teacher;
    }
}
